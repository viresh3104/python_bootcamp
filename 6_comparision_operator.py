# Assigning values to variables
a = 23
b = 27

# Using the equal to operator
equal_to = a == b  # Checks if a is equal to b

# Using the not equal to operator
not_equal_to = a != b  # Checks if a is not equal to b

# Using the greater than operator
greater_than = a > b  # Checks if a is greater than b

# Using the less than operator
less_than = a < b  # Checks if a is less than b

# Using the greater than or equal to operator
greater_than_or_equal = a >= b  # Checks if a is greater than or equal to b

# Using the less than or equal to operator
less_than_or_equal = a <= b  # Checks if a is less than or equal to b

# Printing the results
print(f"a == b: {equal_to}")                 # Output: False
print(f"a != b: {not_equal_to}")             # Output: True
print(f"a > b: {greater_than}")              # Output: False
print(f"a < b: {less_than}")                 # Output: True
print(f"a >= b: {greater_than_or_equal}")    # Output: False
print(f"a <= b: {less_than_or_equal}")       # Output: True
