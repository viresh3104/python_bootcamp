# Assigning values to variables 'a' and 'b'
a = 23
b = 27

# Performing addition
addition = a + b  # Adding a and b

# Performing subtraction
subtraction = a - b  # Subtracting b from a

# Performing multiplication
multiplication = a * b  # Multiplying a and b

# Performing division
division = a / b  # Dividing a by b (result will be a float)

# Performing floor division
floor_division = a // b  # Dividing a by b (result will be an integer, rounded down)

# Performing modulus
modulus = a % b  # Finding the remainder when a is divided by b

# Performing exponentiation
exponentiation = a ** 2  # Raising a to the power of 2

# Printing the results
print(f"a + b = {addition}")        # Output: 50
print(f"a - b = {subtraction}")     # Output: -4
print(f"a * b = {multiplication}")  # Output: 621
print(f"a / b = {division}")        # Output: 0.8518518518518519
print(f"a // b = {floor_division}") # Output: 0
print(f"a % b = {modulus}")         # Output: 23
print(f"a ** 2 = {exponentiation}") # Output: 529
