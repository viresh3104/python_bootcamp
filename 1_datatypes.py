# Assigning an integer value to the variable 'a'
a = 10  # 'a' is an integer

# Assigning a floating-point value to the variable 'b'
b = 3.14  # 'b' is a float

# Assigning a string value to the variable 'c'
c = "Hello, World!"  # 'c' is a string

# Assigning a boolean value to the variable 'd'
d = True  # 'd' is a boolean

# Printing the values and their types
print(f"a = {a}, type: {type(a)}")
print(f"b = {b}, type: {type(b)}")
print(f"c = {c}, type: {type(c)}")
print(f"d = {d}, type: {type(d)}")
