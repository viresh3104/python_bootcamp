# Assigning values to variables
a = True
b = False

# Logical AND operator
and_result = a and b  # Both must be True for the result to be True

# Logical OR operator
or_result = a or b  # At least one must be True for the result to be True

# Logical NOT operator
not_a = not a  # Reverses the boolean value of a

# Printing the results
print(f"a AND b = {and_result}")  # Output: False (since b is False)
print(f"a OR b = {or_result}")    # Output: True (since a is True)
print(f"NOT a = {not_a}")         # Output: False (since a is True and not reverses it)
